        <p>One of the most fundamental questions when developing a strategy for a product is what form it should take: should it be a website or an app? After all, these days more and more people are accessing the internet on their phones, so an app seems like an obvious choice, especially compared to old fashioned websites that are difficult to navigate on a small screen.</p>

        <p>However, modern web design incorporates responsive elements, which can shrink and shift their layout to accommodate different screen sizes. The difference in presentation between an app and a fully responsive website is negligible. This makes it an even harder decision, since the advantages and downsides are less obvious.</p>

        <p>On the one hand, a website is something that necessarily requires an internet connection to be used, so depending on what it is for, it may not be the best approach (H.). Cell service is not universal, and while an app may have code that works no matter what, a website cannot be accessed at all without a connection to the internet.</p>

        <p>But, apps can be more difficult to update than websites(Garbar). Because the user has the whole website on their phone, updates must be downloaded in order to change UX elements as well as content. On a website, everything is loaded from the soldier, so it can be freely changed and updated at any time, and the correct version will always load.</p>

        <p>A website allows for more control on the part of the designers, whereas an app allows for generally better functionality and flexibility for the user. They are both valid choices, and it depends largely on what the goals of the project are.</p>

